function diagnosis_Callback(hObject, eventdata, handles)
% hObject    handle to diagnosis (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA

r = str2double(get(handles.radius, 'String'));
t = str2double(get(handles.texture, 'String'));
p = str2double(get(handles.perimeter, 'String'));
a = str2double(get(handles.area, 'String'));
s = str2double(get(handles.smoothness, 'String'));
cp = str2double(get(handles.compactness, 'String'));
c = str2double(get(handles.concavity, 'String'));
sm = str2double(get(handles.symmetry, 'String'));

kValue = str2double(get(handles.kValue, 'String'));

sample = [r t p a s cp c sm];

inputVar = detectImportOptions('data.csv');
inputVar.SelectedVariableNames=(3:10);
training = readmatrix('data.csv', inputVar);

inputVar.SelectedVariableNames=(2);
group = readmatrix('data.csv', inputVar);

class = fitcknn(training, group, 'NumNeighbors', kValue);
result = predict(class, sample);

if result == "B" 
    set(handles.hasil, 'String', "Tumor jinak terdeteksi");
 elseif result == "M" 
    set(handles.hasil, 'String', "Tumor ganas terdeteksi");
 end

